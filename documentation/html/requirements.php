<font size=6>System Requirements</font><br>
<table border=0 width="100%" cellpadding=5><tr><td bgcolor="#222222">
The game was developed on a 900MHz PC, but it has been tested as marginally-playable on a 233MHz iMac.  In other words, it will most likely run on your computer.<br>
<br>
The only required library is OpenGL, which ships standard on MacOS and Windows and is readily available on GNU/Linux as well.<br>
<br>
<i>Sleep Is Death</i> is <b>two-player only</b>.<br>
<br>
There is no single-player mode.  It is easiest to play over a local network.  Have a friend over and plug in two laptops, or play over local WiFi.  It can be played remotely over the Internet, but you will need to send the other player your Internet address.  <i>Sleep Is Death</i> works across most modern routers and firewalls.  Instructions for remote Internet play are included.
<br>
<br>
Other stuff you will need:
<ul>
<li>keyboard</li>
<li>mouse</li>
<li>speakers or headphones</li>
</ul>
If you are playing in the same room as your friend, it's best if at least one (or both) players use headphones.
